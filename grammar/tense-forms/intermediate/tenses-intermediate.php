        <section class="mb-5 types-section">
            <h2 class="fs-5 mb-3">Types of Intermediate Tenses</h2>
            <p>Below are the tense forms covered at the intermediate level:</p>
            <ul>
                <li><strong>Present Perfect:</strong> have/has + past participle - connects past to present</li>
                <li><strong>Present Perfect Continuous:</strong> have/has been + verb-ing - shows duration up to now</li>
                <li><strong>Past Perfect:</strong> had + past participle - shows which past action happened first</li>
                <li><strong>Past Perfect Continuous:</strong> had been + verb-ing - shows duration before a past moment</li>
                <li><strong>Future Perfect:</strong> will have + past participle - shows completion before a future time</li>
                <li><strong>Future Perfect Continuous:</strong> will have been + verb-ing - shows duration up to a future point</li>
                <li><strong>Future Continuous:</strong> will be + verb-ing - shows action in progress at a future time</li>
            </ul>
        </section>

        <section class="mb-5 explained-section">
            <h2 class="fs-5 mb-3">How Do These Tenses Work?</h2>
            <p>Each intermediate tense has specific uses and structures:</p>
            <ul>
                <li><strong>Present Perfect:</strong> Use for experiences, recent actions, or unfinished time periods. <em>Example: I have visited Paris three times.</em></li>
                <li><strong>Present Perfect Continuous:</strong> Use to emphasize the duration of an action from the past until now. <em>Example: I have been studying English for five years.</em></li>
                <li><strong>Past Perfect:</strong> Use to show which of two past actions happened first. <em>Example: She had left before I arrived.</em></li>
                <li><strong>Past Perfect Continuous:</strong> Use to show an action was in progress before another past action. <em>Example: He had been working there for ten years when he retired.</em></li>
                <li><strong>Future Perfect:</strong> Use to show an action will be completed before a specific future time. <em>Example: By next month, I will have finished this course.</em></li>
                <li><strong>Future Perfect Continuous:</strong> Use to show duration of an action up to a future point. <em>Example: By 2025, they will have been living here for 20 years.</em></li>
                <li><strong>Future Continuous:</strong> Use to show an action in progress at a specific future time. <em>Example: This time tomorrow, I will be flying to Tokyo.</em></li>
            </ul>
        </section>

