            <h2 class="fs-5 mb-3">What are Tenses?</h2>
            <p>Tenses tell us <strong>when</strong> something happens. In English, we use different verb forms to show if something happened in the past, is happening now, or will happen in the future. Understanding tenses helps you talk about time correctly.</p>
        </section>

        <section class="mb-5 types-section">
            <h2 class="fs-5 mb-3">Types of Tenses (Beginner Level)</h2>
            <p>At the beginner level, we focus on five important tenses:</p>
            <ul>
                <li><strong>Present Simple:</strong> For habits, facts, and routines. Example: <em>I eat breakfast every day.</em></li>
                <li><strong>Past Simple:</strong> For completed actions in the past. Example: <em>I walked to school yesterday.</em></li>
                <li><strong>Future Simple:</strong> For predictions, decisions, and plans. Example: <em>I will study tomorrow.</em></li>
                <li><strong>Present Continuous:</strong> For actions happening right now. Example: <em>I am eating lunch.</em></li>
                <li><strong>Past Continuous:</strong> For actions that were in progress in the past. Example: <em>I was sleeping when you called.</em></li>
            </ul>
        </section>

        <section class="mb-5 explained-section">
            <h2 class="fs-5 mb-3">How Do Tenses Work?</h2>
            <p>Each tense has a specific structure and use. Let's look at how they work:</p>
            <ul>
                <li><strong>Present Simple:</strong> Use the base form of the verb (add -s/-es for he/she/it). <em>Example: He works every day.</em></li>
                <li><strong>Past Simple:</strong> Use the past form of the verb (usually add -ed, but many verbs are irregular). <em>Example: She played tennis yesterday.</em></li>
                <li><strong>Future Simple:</strong> Use "will" + base form of the verb. <em>Example: They will arrive tomorrow.</em></li>
                <li><strong>Present Continuous:</strong> Use am/is/are + verb-ing. <em>Example: I am reading a book now.</em></li>
                <li><strong>Past Continuous:</strong> Use was/were + verb-ing. <em>Example: We were watching TV at 8 PM.</em></li>
            </ul>
        </section>

