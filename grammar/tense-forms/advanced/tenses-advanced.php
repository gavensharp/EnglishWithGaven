            <h2 class="fs-5 mb-3">Complex Tense Functions and Nuances</h2>
            <p>Let's explore the sophisticated functions and distinctions in advanced tense usage:</p>

            <h3 class="fs-6 mb-2 mt-4">Mixed Conditionals</h3>
            <ul>
                <li><strong>Past condition â†’ Present result:</strong> If I had taken that job (but I didn't), I would be living in Paris now.</li>
                <li><strong>Present condition â†’ Past result:</strong> If I were more organized (but I'm not), I wouldn't have missed the deadline.</li>
                <li><strong>Past condition â†’ Future result:</strong> If she had accepted the offer, she would be starting next week.</li>
            </ul>

            <h3 class="fs-6 mb-2 mt-4">Narrative Tense Sequences</h3>
            <ul>
                <li><strong>Past Perfect for earlier background:</strong> She realized she had left her phone at home (action before main narrative).</li>
                <li><strong>Past Perfect Continuous for duration before past:</strong> They had been traveling for 12 hours when they finally reached the border.</li>
                <li><strong>Past Simple for main narrative events:</strong> He opened the door and walked inside.</li>
                <li><strong>Past Continuous for atmosphere/background:</strong> The sun was setting as they approached the city.</li>
            </ul>

            <h3 class="fs-6 mb-2 mt-4">Future in the Past</h3>
            <ul>
                <li><strong>"would" for future from past perspective:</strong> She didn't know she would become famous.</li>
                <li><strong>"was going to" for past plans:</strong> I was going to call you, but I forgot.</li>
                <li><strong>"was about to" for imminent past action:</strong> The show was about to start when the power went out.</li>
                <li><strong>"was to" (formal) for destined events:</strong> This was to be their last meeting.</li>
            </ul>

            <h3 class="fs-6 mb-2 mt-4">Reported Speech Complexities</h3>
            <ul>
                <li><strong>Backshifting:</strong> "I am tired" â†’ She said she was tired.</li>
                <li><strong>No backshift for permanent truths:</strong> He said the Earth revolves around the Sun (not "revolved").</li>
                <li><strong>No backshift for recent speech:</strong> She's just told me she feels better (not "felt").</li>
                <li><strong>Modal backshifting:</strong> can â†’ could, will â†’ would, may â†’ might</li>
            </ul>

            <h3 class="fs-6 mb-2 mt-4">Important Distinctions</h3>
            <ul>
                <li><strong>Present Perfect vs. Present Perfect Continuous:</strong> "I have read the book" (completed) vs. "I have been reading the book" (ongoing/recently finished with emphasis on activity).</li>
                <li><strong>Past Simple vs. Present Perfect:</strong> "I lived in Paris for 3 years" (finished period) vs. "I have lived in Paris for 3 years" (still living there).</li>
                <li><strong>Past Perfect vs. Past Simple:</strong> When the sequence is clear from context, past simple can replace past perfect: "After he finished dinner, he watched TV" (sequence clear without "had finished").</li>
                <li><strong>Will vs. Going to:</strong> "I'll help you" (spontaneous decision) vs. "I'm going to help you" (planned intention).</li>
            </ul>
