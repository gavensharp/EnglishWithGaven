        <section class="mb-5 types-section">
            <h2 class="fs-5 mb-3">Advanced Tense Structures</h2>
            <p>Advanced tense usage encompasses several sophisticated structures:</p>
            <ul>
                <li><strong>Mixed Conditionals:</strong> Combining different time frames in conditional sentences to express complex hypothetical situations. Example: <em>If I had studied harder (past), I would be more confident now (present).</em></li>
                <li><strong>Narrative Tenses:</strong> Using past perfect, past perfect continuous, and past simple together to create cohesive narratives with multiple time layers. Example: <em>She had been waiting for hours when he finally arrived, having forgotten about their appointment.</em></li>
                <li><strong>Future in the Past:</strong> Expressing future actions from a past perspective using "would," "was going to," or "was about to." Example: <em>He didn't know that his life was about to change forever.</em></li>
                <li><strong>Tense Sequences in Reported Speech:</strong> Understanding backshifting and when to maintain original tenses in indirect speech. Example: <em>She said she had finished the report (backshift) vs. The teacher said that water boils at 100Â°C (permanent truth).</em></li>
                <li><strong>Aspectual Distinctions:</strong> Understanding how perfect and continuous aspects interact to create precise meanings. Example: <em>I have lived here for 10 years (still living) vs. I have been living here for 10 years (emphasis on duration/temporary).</em></li>
                <li><strong>Stative vs. Dynamic Verbs:</strong> Recognizing which verbs can and cannot be used in continuous forms, and understanding exceptions. Example: <em>I'm having dinner (dynamic) vs. I have a car (stative).</em></li>
                <li><strong>Historical Present:</strong> Using present tenses to create immediacy when recounting past events. Example: <em>So I'm walking down the street when suddenly this guy comes up to me...</em></li>
            </ul>
        </section>

