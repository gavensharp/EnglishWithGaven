      <h2 class="fs-5 mb-3">What are Prepositions?</h2>
      <p>Prepositions are words that tell us where, when, or how something happens. They connect a noun or verbs with other words in a sentence. Prepositions help us show the time, place, or direction of something.</p>
    </section>

    <section class="mb-5 types-section">
      <h2 class="fs-5 mb-3">Types of Prepositions</h2>
      <p>There are different kinds of prepositions. Let's look at some of them:</p>
      <ul>
        <li><strong>Prepositions of Place:</strong> These tell us where something is. Examples: <em>in, on, at, under, above, next to, near, behind, between, by</em>.</li>
        <li><strong>Prepositions of Time:</strong> These tell us when something happens. Examples: <em>at, on, in</em>.</li>
        <li><strong>Prepositions of Direction/Movement:</strong> These show movement to a place. Examples: <em>to, into, onto, over</em>.</li>
        <li><strong>Prepositions of Manner:</strong> These show how something is done. Examples: <em>by, with</em>.</li>
      </ul>
    </section>

    <section class="mb-5 explained-section">
      <h2 class="fs-5 mb-3">What do they do?</h2>
      <p>Prepositions help to explain where, when, or how something happens. For example:</p>
      <ul>
        <li><strong>In</strong>: We use "in" for large places, like countries, cities, or rooms. And to say that something is in something else.</li>
        <li><strong>On</strong>: We use "on" for surfaces, like tables or walls. And to say that something is on something else.</li>
        <li><strong>At</strong>: We use "at" for specific places or times, like "at the bus stop" or "at 5 o'clock."</li>
        <li><strong>Under</strong>: We use "under" to show that something is being covered something else.</li>
        <li><strong>Above</strong>: We use "above" to show that something is higher than something else but not touching it.</li>
        <li><strong>Next to</strong>: We use "next to" to show that something is very close to the side of something else.</li>
        <li><strong>Near</strong>: We use "near" to show that something is close to something else.</li>
        <li><strong>Behind</strong>: We use "behind" to show that something is at the back of something else.</li>
        <li><strong>Between</strong>: We use "between" to show that something is in the middle of two things.</li>
        <li><strong>On</strong>: We use "on" as a preposition of time to indicate specific days or dates.</li>
        <li><strong>In</strong>: We use "in" as a preposition of time to say something happens in a period of time, such as seasons, months, or years.</li>
        <li><strong>To</strong>: We use "to" to say something is going from one place to another place.</li>
        <li><strong>Into</strong>: We use "into" to say something is going from outside to inside a place.</li>
        <li><strong>Onto</strong>: We use "onto" to say something is going to the top of something else.</li>
        <li><strong>Over</strong>: We use "over" as a preposition of movement to say that something is going above or higher than something else.</li>
        <li><strong>By</strong>: We use "by" to show the position of something close to something else, or that something is being done by someone.</li>
        <li><strong>With</strong>: We use "with" to show that someone is doing something with someone else.</li>
      </ul>
    </section>

