    <section class="mb-5 types-section">
      <h2 class="fs-5 mb-3">Advanced Prepositional Structures</h2>
      <p>Prepositions can serve a variety of complex functions in a sentence. Here are some of the key advanced structures:</p>
      <ul>
        <li><strong>Verb-Preposition Pairs (Prepositional Verbs):</strong> Verbs that require specific prepositions to convey their intended meaning. The preposition is essential to the verb's meaning. Examples: <em>rely on, participate in, insist on, account for, apologize for, care for, depend on, object to, wait for</em>.</li>
        <li><strong>Phrasal Prepositions:</strong> Multi-word expressions that function as a single preposition, often adding nuance or formality. Examples: <em>in spite of, out of spite, in accordance with, by means of, on behalf of, with regard to, in lieu of, by virtue of</em>.</li>
        <li><strong>Compound Prepositions:</strong> Prepositions made up of two or more words acting as a single unit to express complex relationships. Examples: <em>because of, due to, except for, instead of, apart from, aside from, according to, as for</em>.</li>
        <li><strong>Complex Prepositional Phrases:</strong> Extended phrases where the preposition combines with modifiers and objects to create sophisticated expressions. Examples: <em>in the wake of, in front of, on top of, at the behest of, in the midst of, for the sake of, at the expense of, on the verge of</em>.</li>
        <li><strong>Adjective-Preposition Collocations:</strong> Fixed combinations of adjectives with specific prepositions. Examples: <em>fond of, keen on, aware of, capable of, responsible for, satisfied with, familiar with, interested in</em>.</li>
        <li><strong>Noun-Preposition Collocations:</strong> Fixed combinations of nouns with specific prepositions. Examples: <em>effect on, impact on, influence on, access to, approach to, reaction to, solution to, answer to</em>.</li>
      </ul>
    </section>

    <section class="mb-5 explained-section">
      <h2 class="fs-5 mb-3">Prepositional Functions and Nuances</h2>
      <p>Let's explore the advanced functions and subtle distinctions in prepositional usage:</p>

      <h3 class="fs-6 mb-2 mt-4">Verb-Preposition Pairs</h3>
      <ul>
        <li><strong>rely on:</strong> To depend on or trust someone/something (cannot use *rely for/at).</li>
        <li><strong>participate in:</strong> To take part in an activity (not *participate at/on).</li>
        <li><strong>insist on:</strong> To demand firmly or refuse to change one's opinion about something.</li>
        <li><strong>account for:</strong> To explain or provide a reason for something.</li>
        <li><strong>apologize for:</strong> To express regret for an action or mistake.</li>
        <li><strong>object to:</strong> To express disagreement or opposition to something.</li>
        <li><strong>wait for:</strong> To remain in a place until someone/something arrives.</li>
      </ul>

      <h3 class="fs-6 mb-2 mt-4">Phrasal Prepositions</h3>
      <ul>
        <li><strong>in spite of:</strong> Despite; used to introduce a contrast (more formal than "despite").</li>
        <li><strong>out of spite:</strong> Motivated by malice or desire to hurt someone.</li>
        <li><strong>in accordance with:</strong> In agreement with; following a rule or standard.</li>
        <li><strong>by means of:</strong> Through the use of; by using a particular method.</li>
        <li><strong>on behalf of:</strong> As a representative of; in the interests of someone.</li>
        <li><strong>with regard to:</strong> Concerning; about (formal usage).</li>
        <li><strong>in lieu of:</strong> Instead of; as a substitute for.</li>
        <li><strong>by virtue of:</strong> Because of; as a result of a particular quality or position.</li>
      </ul>

      <h3 class="fs-6 mb-2 mt-4">Compound Prepositions</h3>
      <ul>
        <li><strong>because of:</strong> For the reason that; introduces cause.</li>
        <li><strong>due to:</strong> Caused by; attributable to (often follows "be").</li>
        <li><strong>except for:</strong> Apart from; with the exception of.</li>
        <li><strong>instead of:</strong> As an alternative to; in place of.</li>
        <li><strong>apart from:</strong> In addition to; except for (can have both meanings depending on context).</li>
        <li><strong>according to:</strong> As stated by; in agreement with.</li>
      </ul>

      <h3 class="fs-6 mb-2 mt-4">Complex Prepositional Phrases</h3>
      <ul>
        <li><strong>in the wake of:</strong> Following as a consequence; in the aftermath of.</li>
        <li><strong>at the behest of:</strong> At the request or command of someone.</li>
        <li><strong>in the midst of:</strong> In the middle of; during.</li>
        <li><strong>for the sake of:</strong> For the purpose of; in order to achieve.</li>
        <li><strong>at the expense of:</strong> At the cost or sacrifice of something else.</li>
        <li><strong>on the verge of:</strong> Very close to; about to.</li>
      </ul>

      <h3 class="fs-6 mb-2 mt-4">Important Distinctions</h3>
      <ul>
        <li><strong>"on time" vs "in time":</strong> "On time" means punctual/at the scheduled time; "in time" means with time to spare/before it's too late.</li>
        <li><strong>"at the end" vs "in the end":</strong> "At the end" refers to a physical location or specific point; "in the end" means finally or ultimately.</li>
        <li><strong>"made of" vs "made from":</strong> "Made of" shows material that hasn't changed form; "made from" shows material that has been transformed.</li>
        <li><strong>"bored with" vs "bored of":</strong> "Bored with" is standard; "bored of" is informal British English.</li>
      </ul>
    </section>

