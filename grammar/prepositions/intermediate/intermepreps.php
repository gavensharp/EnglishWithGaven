    <section class="mb-5 intro-section">
      <h2 class="fs-5 mb-3">What are Prepositions?</h2>
      <p>Prepositions are words that connect verbs or nouns or pronouns with other words in a sentence to form prepositional phrases. These phrases provide extra details about time, place, or direction. Understanding how prepositions work with verbs and nouns helps clarify the meaning of a sentence, making your speaking and writing more accurate.</p>
    </section>

    <section class="mb-5 types-section">
      <h2 class="fs-5 mb-3">Types of Prepositions</h2>
      <p>Below is an extended list of preposition categories often encountered at the intermediate level:</p>
      <ul>
        <li><strong>Prepositions of Place:</strong> in, on, at, under, above, near, between, among, beside, behind, beyond, opposite, across, adjacent to, in front of, on top of, next to, below, beneath, underneath, against</li>
        <li><strong>Prepositions of Time:</strong> at, on, in, during, before, after, since, until, by</li>
        <li><strong>Prepositions of Direction/Movement:</strong> to, from, out of, into, out, onto, through, toward, up, down, across, around, past, over, along</li>
        <li><strong>Prepositions of Manner:</strong> by, with, like, as</li>
        <li><strong>Prepositions of Cause/Reason:</strong> because of, due to, owing to, on account of, in response to</li>
        <li><strong>Prepositions of Agent/Instrument:</strong> by, via, like, with and on</li>
        <li><strong>Compound Prepositions:</strong> in front of, on top of, out of, next to, in spite of, adjacent to</li>
        <li><strong>Prepositions of Comparison:</strong> like, as, than</li>
        <li><strong>Prepositions of Measure:</strong> per, by (as in "miles per hour")</li>
        <li><strong>Prepositions of Source:</strong> from, out of</li>
        <li><strong>Prepositions of Possession:</strong> of, with</li>
      </ul>
    </section>

    <section class="mb-5 explained-section">
      <h2 class="fs-5 mb-3">What do they do?</h2>
      <p>Now that we have learned more prepositions, let's learn more about what they do.</p>
      <ul>

        <li><strong>Among</strong>: We use "among" to indicate being surrounded by something or included within a group.</li>
        <li><strong>Beside</strong>: We use "beside" to show that something is next to or at the side of something else.</li>
        <li><strong>Against</strong>: We use "against" to indicate opposition or resistance, showing that something is in conflict with or contrary to another thing.</li>
        <li><strong>Beyond</strong>: We use "beyond" to show that something is further away than something else.</li>
        <li><strong>Opposite</strong>: We use "opposite" to show that something is facing or across from something else.</li>
        <li><strong>Across</strong>: We use "across" to show that something is on the other side of something else.</li>
        <li><strong>Adjacent to</strong>: We use "adjacent to" to show that something is next to or near something else at an angle.</li>
        <li><strong>In front of</strong>: We use "in front of" to show that something is ahead of something else.</li>
        <li><strong>On top of</strong>: We use "on top of" to show that something is directly above something else.</li>
        <li><strong>In front of</strong>: We use "in front of" to show that something is ahead of something else. This is opposite to behind.</li>
        <li><strong>Below</strong>: We use "below" to show that something is lower than something else.</li>
        <li><strong>Beneath</strong>: We use "beneath" to show that something is under something else. It is often used in place of "under" to be more polite.</li>
        <li><strong>Underneath</strong>: We use "underneath" to show that something is directly or closely below something else.</li>
        <li><strong>During</strong>: We use "during" to show that something happens within a specific time period.</li>
        <li><strong>Before</strong>: We use "before" to show that something happens earlier than something else.</li>
        <li><strong>After</strong>: We use "after" to show that something happens later than something else.</li>
        <li><strong>Since</strong>: We use "since" to show that something has been happening from a specific time until now.</li>
        <li><strong>Until</strong>: We use "until" to show that something continues up to a specific time.</li>
        <li><strong>By</strong>: We use "by" to show that something happens before a specific time.</li>
        <li><strong>To</strong>: We use "to" to show movement towards a place or direction.</li>
        <li><strong>From</strong>: We use "from" to show movement away from a place or direction.</li>
        <li><strong>From</strong>: We use "from" as a source preposition to indicate the origin of something or who something is coming from.</li>
        <li><strong>Out of</strong>: We use "out of" to show movement from inside to outside a place.</li>
        <li><strong>Into</strong>: We use "into" to show movement from outside to inside a place.</li>
        <li><strong>Out</strong>: We use "out" to show movement from inside to outside a place.</li>
        <li><strong>Through</strong>: We use "through" to show movement from one side to the other side through the inside of something.</li>
        <li><strong>Toward</strong>: We use "toward" to show movement in the direction of something.</li>
        <li><strong>Up</strong>: We use "up" to show movement to a higher place or position.</li>
        <li><strong>Down</strong>: We use "down" to show movement to a lower place or position.</li>
        <li><strong>Across</strong>: We use "across" to show movement from one side to the other side.</li>
        <li><strong>Around</strong>: We use "around" to show movement in a circular motion to the other side of something.</li>
        <li><strong>Past</strong>: We use "past" to show movement beyond something.</li>
        <li><strong>Over</strong>: We use "over" to show that something is moving above or higher than something else.</li>
        <li><strong>Along</strong>: We use "along" to describe how two things move in the same direction or do something together.</li>
        <li><strong>By</strong>: We use "by" as a preposition of manner to indicate the method or means by which an action is performed.</li>
        <li><strong>With</strong>: We use "with" as a preposition of manner to indicate the tool or instrument used to perform an action.</li>
        <li><strong>Like</strong>: We use "like" as a preposition of manner to indicate similarity or resemblance.</li>
        <li><strong>As</strong>: We use "as" as a preposition of manner to indicate the role or function of someone or something.</li>
        <li><strong>Because of</strong>: We use "because of" to show the reason or cause of something.</li>
        <li><strong>Due to</strong>: We use "due to" to show the reason or cause of something.</li>
        <li><strong>Owing to</strong>: We use "owing to" to show the reason or cause of something.</li>
        <li><strong>On account of</strong>: We use "on account of" to show the reason or cause of something.</li>
        <li><strong>In response to</strong>: We use "in response to" to express that something is responding to something else.</li>
        <li><strong>By</strong>: We use "by" as a preposition of Agent/Instrument to indicate who or what is performing the action.</li>
        <li><strong>Via</strong>: We use "via" as a preposition of Agent/Instrument to indicate the means or route by which an action is performed.</li>


      </ul>
    </section>


